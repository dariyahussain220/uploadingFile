CREATE TABLE `fileInfo`(
  id BIGINT(8) PRIMARY KEY NOT NULL AUTO_INCREMENT,
  size BIGINT(8) NOT NULL,
  fileName varchar(216) NOT NULL,
  description varchar(512) NOT NULL,
  filepath varchar(512) NOT NULL,
  createdOn timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updatedOn timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3)  
)ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=latin1;

